    def nextData(self):
        done = False
        while( not done ):
            stat = self.getStatus()
            done = stat & BMA456._CNT_STATUS_DRDY_ACC
        return self.lastData()

    def lastData(self):
        data = self.getAccData()
        time = self.getSensorTime()
        data[0] = self.transfer( data[0] )
        data[1] = self.transfer( data[1] )
        data[2] = self.transfer( data[2] )
        data.insert( 0, time )
        return data

    #
    # Configures the data range.
    # newRange: The new range to set. Valid values are ACC_RANGE_xxx.
    # Raises ValueError in case of parameter is out of range.
    def setRange( self, newRange ):
        if (newRange < BMA456.ACC_RANGE_MIN) or (newRange > BMA456.ACC_RANGE_MAX):
            raise ValueError('Parameter out of range: '+str(newRange)+'. Use BMA456.ACC_RANGE_xxx mnemonics!')
        else:
            self.setReg( BMA456._REG_ACC_RANGE, newRange )
            self.scale_mg = BMA456._RANGE2SCALE.get( newRange, BMA456._FULL_SCALE_2G )
            Sensor.setRange( self, newRange )

    #
    # Configures the data update rate.
    # newRate: The new data rate. Valid Values are ACC_DATARATE_xxx, meant as Hertz.
    # Raises ValueError in case of parameter is out of range.
    #
    def setDataRate( self, newRate ):
        # Configure output data rate: Averaging mode, 2 repetitions
        if (newRate < BMA456.ACC_DATARATE_MIN) or (newRate > BMA456.ACC_DATARATE_MAX):
            raise ValueError('Parameter out of range: '+str(newRate)+'. Use BMA456.ACC_DATARATE_xxx mnemonics!')
        else:
            self.setReg( BMA456._REG_ACC_CONF, BMA456._CNT_ACC_CONF_PERF_MODE_AVG | BMA456._CNT_ACC_CONF_BWP_OSR2_AVG2 | newRate )
            Sensor.setDataRate( self, newRate )

