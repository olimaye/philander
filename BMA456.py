    #
    # Transfer function to translate digital measurement reading into
    # physical dimension value.
    # return: acceleration in milli-g
    #
    def transfer( self, reading ):
        ret = reading
        if reading > 0x7FFF:
            ret = reading - 0x10000
            ret = ret * self.scale_mg - 0x4000
        else:
            ret = ret * self.scale_mg + 0x4000
        ret = ret >> 15
        return ret

