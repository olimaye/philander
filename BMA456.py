    #
    # Initializes the sensor
    # Raises OSError in case of problems with the connection.
    #        OSError on failure when initializing the chip.
    #
    def init(self):
        # Call serial driver's init  method to finalize communication setup
        SerialDevice.init(self)
        
        # Test address
        self.testConnection()
        
        # Initialization sequence for interrupt feature engine
        # Disable advanced power save mode: PWR_CONF.adv_power_save = 0.
        self.setReg( BMA456._REG_PWR_CONF, BMA456._CNT_PWR_CONF_ADV_PWR_SAVE_DISABLE )
        # Wait for 450 us.
        self.waitMicro( 500 )   
        # Write INIT_CTRL.init_ctrl=0x00
        self.setReg( BMA456._REG_INIT_CTRL, 0 )
        # Load configuration file
        self.putConfigFile()
        # Enable sensor features: write 0x01 into register INIT_CTRL.init_ctrl.
        # This operation must not be performed more than once after POR or softreset.
        self.setReg( BMA456._REG_INIT_CTRL, 1 )
       
        # Check status of the interrupt feature engine
        # Wait until Register INTERNAL_STATUS.message contains the value 1. This will happen after at most 140-150 msec.
        self.waitMicro( 150000 )
        istat = self.getInternalStatus()
        if (istat & BMA456._CNT_INTERNAL_STATUS_MSG) != BMA456._CNT_INTERNAL_STATUS_MSG_INIT_OK:
            raise OSError('BMA456 initialization failed.')
           
        # After initialization sequence has been completed, the device is in
        # configuration mode (power mode). Now it is possible to switch to the
        # required power mode and all features are ready to use.
       
        # Enable accelerometer
        self.setReg( BMA456._REG_PWR_CTRL, BMA456._CNT_PWR_CTRL_ACC_ENABLE | BMA456._CNT_PWR_CTRL_AUX_DISABLE )
       
        # Configure power save mode:
        self.setReg( BMA456._REG_PWR_CONF, BMA456._CNT_PWR_CONF_ADV_PWR_SAVE_DISABLE | BMA456._CNT_PWR_CONF_FIFO_WKUP_DISABLE )
        
        # Call Sensor's init  method to set range and data rate
        Sensor.init(self)


