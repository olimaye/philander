    def putConfigFile(self):
        for idx in range(0, len(BMA456._CONFIG_FILE), BMA456._CHUNK_SIZE):
            chunk = BMA456._CONFIG_FILE[idx : idx+BMA456._CHUNK_SIZE]
            widx = int(idx/2)
            self.setReg( BMA456._REG_DMA_LOW, widx & 0x0F )
            self.setReg( BMA456._REG_DMA_HI, widx >> 4 )
            self.writeBlock( BMA456._REG_FEATURES, chunk )

    #
    # Actively waits for a specific duration.
    # duration: The amount of time to wait, given in microseconds.
    #
    def waitMicro( self, duration ):
        if duration > 0:
            tNow = self.getSensorTime()
            tLimit = int(tNow + duration) & 0xFFFFFFFF
            if tLimit < tNow:
                t2 = self.getSensorTime()
                while t2 > tNow:
                    tNow = t2
                    t2 = self.getSensorTime()
                tNow = t2
            while tNow < tLimit:
                tNow = self.getSensorTime()

