    # Sensor time in microseconds.
    def getSensorTime(self):
        #t0 = read_data( _REG_SENSOR_TIME0 )
        #t1 = read_data( _REG_SENSOR_TIME1 )
        #t2 = read_data( _REG_SENSOR_TIME2 )
        [t0, t1, t2] = self.readBlock( BMA456._REG_SENSOR_TIME, 3 )
        t = (t2<<16) + (t1<<8) + t0
        # Sensortime increments every 39.0625 µs.
        # 39.0625 = 625/16 = 39 + 1/16.
        t = t*39 + ( (t+8) >> 4 )
        return t

